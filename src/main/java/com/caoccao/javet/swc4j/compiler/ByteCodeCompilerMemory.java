package com.caoccao.javet.swc4j.compiler;

import com.caoccao.javet.swc4j.ast.interfaces.ISwc4jAst;
import com.caoccao.javet.swc4j.compiler.memory.*;
import com.caoccao.javet.swc4j.exceptions.Swc4jByteCodeCompilerException;

import java.util.HashMap;
import java.util.Map;

/**
 * Memory management for the bytecode compiler, tracking all scoped registries and compilation state.
 */
public final class ByteCodeCompilerMemory {
    private final Map<String, byte[]> byteCodeMap;
    private final ScopedCompilationContext scopedCompilationContext;
    private final ScopedFunctionalInterfaceRegistry scopedFunctionalInterfaceRegistry;
    private final ScopedJavaTypeRegistry scopedJavaTypeRegistry;
    private final ScopedPackage scopedPackage;
    private final ScopedSourceCode scopedSourceCode;
    private final ScopedStandaloneFunctionRegistry scopedStandaloneFunctionRegistry;
    private final ScopedTemplateCacheRegistry scopedTemplateCacheRegistry;
    private final ScopedTypeAliasRegistry scopedTypeAliasRegistry;
    private int fnInterfaceCounter;

    /**
     * Constructs a new ByteCodeCompilerMemory instance.
     */
    public ByteCodeCompilerMemory() {
        byteCodeMap = new HashMap<>();
        scopedCompilationContext = new ScopedCompilationContext();
        scopedFunctionalInterfaceRegistry = new ScopedFunctionalInterfaceRegistry();
        scopedJavaTypeRegistry = new ScopedJavaTypeRegistry();
        scopedPackage = new ScopedPackage();
        scopedSourceCode = new ScopedSourceCode();
        scopedStandaloneFunctionRegistry = new ScopedStandaloneFunctionRegistry();
        scopedTemplateCacheRegistry = new ScopedTemplateCacheRegistry();
        scopedTypeAliasRegistry = new ScopedTypeAliasRegistry();
        fnInterfaceCounter = 0;
    }

    /**
     * Enters a new scope for all scoped registries.
     */
    public void enterScope() {
        scopedFunctionalInterfaceRegistry.enterScope();
        scopedSourceCode.enterScope();
        scopedStandaloneFunctionRegistry.enterScope();
        scopedTypeAliasRegistry.enterScope();
    }

    /**
     * Exits the current scope for all scoped registries.
     */
    public void exitScope() {
        scopedFunctionalInterfaceRegistry.exitScope();
        scopedSourceCode.exitScope();
        scopedStandaloneFunctionRegistry.exitScope();
        scopedTypeAliasRegistry.exitScope();
    }

    /**
     * Get the bytecode map containing generated bytecode for all compiled classes.
     *
     * @return the bytecode map
     */
    public Map<String, byte[]> getByteCodeMap() {
        return byteCodeMap;
    }

    /**
     * Get the current CompilationContext.
     *
     * @return the current compilation context
     */
    public CompilationContext getCompilationContext() {
        return scopedCompilationContext.current();
    }

    /**
     * Get the next functional interface counter value and increment.
     *
     * @return the next counter value
     */
    public int getNextFnInterfaceCounter() {
        return ++fnInterfaceCounter;
    }

    /**
     * Gets the scoped compilation context storage.
     *
     * @return the scoped compilation context
     */
    public ScopedCompilationContext getScopedCompilationContext() {
        return scopedCompilationContext;
    }

    /**
     * Gets the scoped functional interface registry.
     *
     * @return the scoped functional interface registry
     */
    public ScopedFunctionalInterfaceRegistry getScopedFunctionalInterfaceRegistry() {
        return scopedFunctionalInterfaceRegistry;
    }

    /**
     * Gets the scoped Java type registry.
     *
     * @return the scoped Java type registry
     */
    public ScopedJavaTypeRegistry getScopedJavaTypeRegistry() {
        return scopedJavaTypeRegistry;
    }

    /**
     * Get the scoped package storage.
     *
     * @return the scoped package
     */
    public ScopedPackage getScopedPackage() {
        return scopedPackage;
    }

    /**
     * Get the scoped source code storage.
     *
     * @return the scoped source code
     */
    public ScopedSourceCode getScopedSourceCode() {
        return scopedSourceCode;
    }

    /**
     * Gets the scoped standalone function registry.
     *
     * @return the scoped standalone function registry
     */
    public ScopedStandaloneFunctionRegistry getScopedStandaloneFunctionRegistry() {
        return scopedStandaloneFunctionRegistry;
    }

    /**
     * Get the scoped template cache registry for tagged template literal optimization.
     *
     * @return the scoped template cache registry
     */
    public ScopedTemplateCacheRegistry getScopedTemplateCacheRegistry() {
        return scopedTemplateCacheRegistry;
    }

    /**
     * Gets the scoped type alias registry.
     *
     * @return the scoped type alias registry
     */
    public ScopedTypeAliasRegistry getScopedTypeAliasRegistry() {
        return scopedTypeAliasRegistry;
    }

    /**
     * Pop the current compilation context from the stack.
     * Used when exiting a nested scope (like a lambda).
     *
     * @return the popped context
     */
    public CompilationContext popCompilationContext() {
        return scopedCompilationContext.exitScope();
    }

    /**
     * Push a new compilation context onto the stack.
     * Used when entering a nested scope (like a lambda).
     *
     * @param isStatic true if the new context is for a static method
     * @return the new context
     */
    public CompilationContext pushCompilationContext(boolean isStatic) {
        return scopedCompilationContext.enterScope(isStatic);
    }

    /**
     * Resets all compiler memory state including all registries and bytecode.
     */
    public void reset() {
        byteCodeMap.clear();
        fnInterfaceCounter = 0;
        scopedCompilationContext.clear();
        scopedFunctionalInterfaceRegistry.clear();
        scopedJavaTypeRegistry.clear();
        scopedPackage.clear();
        scopedSourceCode.clear();
        scopedStandaloneFunctionRegistry.clear();
        scopedTemplateCacheRegistry.clear();
        scopedTypeAliasRegistry.clear();
    }

    /**
     * Resets the current compilation context to its initial state.
     */
    public void resetCompilationContext() {
        scopedCompilationContext.resetCurrent();
    }

    /**
     * Reset the compilation context for a specific method type.
     *
     * @param isStatic true if this is for a static method (no 'this' parameter)
     */
    public void resetCompilationContext(boolean isStatic) {
        scopedCompilationContext.resetCurrent(isStatic);
    }

    /**
     * Resolves a simple class name to its JavaTypeInfo.
     * First checks the type alias registry, then the java type registry.
     *
     * @param className the class name to resolve
     * @param ast       the AST node for error reporting
     * @return the JavaTypeInfo
     * @throws Swc4jByteCodeCompilerException if the type is not found
     */
    public JavaTypeInfo resolveType(String className, ISwc4jAst ast) throws Swc4jByteCodeCompilerException {
        // First try to resolve from the type alias registry
        String resolvedType = scopedTypeAliasRegistry.resolve(className);
        if (resolvedType != null) {
            String internalName = resolvedType.replace('.', '/');
            int lastSlash = internalName.lastIndexOf('/');
            String packageName = lastSlash >= 0 ? resolvedType.substring(0, lastSlash).replace('/', '.') : "";
            return new JavaTypeInfo(className, packageName, internalName);
        }

        // Then try to resolve from the java type registry
        var javaTypeInfo = scopedJavaTypeRegistry.resolve(className);
        if (javaTypeInfo != null) {
            return javaTypeInfo;
        }

        throw new Swc4jByteCodeCompilerException(
                scopedSourceCode.getSourceCode(),
                ast,
                "Type '" + className + "' not found. Did you forget to import it?");
    }
}
